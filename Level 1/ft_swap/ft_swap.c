#include <stdio.h>

void ft_swap(int *a, int *b)
{
    int temp = *a;
    *a = *b;
    *b = temp;
}

int main(void)
{
    int a = 2;
    int b = 3;
    ft_swap(&a, &b);
    printf("a: %d, b: %d", a , b);
}